template<typename T>
struct Point3{
    Point3(T a, T b, T c):
        x(a), y(b), z(c)
    {}

    T x;
    T y;
    T z;
};

using Point3f = Point3<float>;

struct Mesh{
    std::string name;
    std::vector<Point3f> vertices;
    std::vector<Point3f> normals;

    void dump(){
        printf("o %s\n", name.c_str());
        for(auto p : vertices){
            printf("v %f %f %f\n", p.x, p.y, p.z);
        }
        for(auto p : normals){
            printf("vn %f %f %f\n", p.x, p.y, p.z);
        }
    }
};

struct WavefrontFormat{
    int row = 0;
    int col = 0;
    char c = ' ';
    Mesh mesh;

    char getc(FILE* file){
        col += 1;
        c = char(fgetc(file));
        if (c == '\n'){
            row += 1;
            col = 0;
        }
        return c;
    }

    void parse_name(FILE* file){
        for(char i = getc(file); c != '\n'; i = getc(file)){
            if (i != ' '){
                mesh.name += i;
            }
        }
    }

    void parse_normal(FILE* file){
        float x = 0, y = 0, z = 0;
        fscanf(file, " %f %f %f", &x, &y , &z);
        mesh.normals.emplace_back(x, y , z);
    }

    void parse_vertex(FILE* file){
        float x = 0, y = 0, z = 0;
        fscanf(file, " %f %f %f", &x, &y , &z);
        mesh.vertices.emplace_back(x, y , z);
    }

    Mesh parse(std::string const& file_name){
        FILE* file = fopen(file_name.c_str(), "r");
        assert(file != nullptr && "file does not exit");

        do{
            c = getc(file);
            if (c == '#'){
                while((c = getc(file)) != '\n'){}
                continue;
            }

            if (col == 1){
                if (c == 'o') parse_name(file);

                if (c == 'v'){
                    c = getc(file);
                    if (c == 'n')
                        parse_normal(file);
                    else
                        parse_vertex(file);
                }
            }

            row += 1;
        }while(c != EOF);

        fclose(file);
        return mesh;
    }
};

Mesh parse_obj(std::string const& file){
    return WavefrontFormat().parse(file);
}




